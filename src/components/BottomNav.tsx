export default function BottomNav() {
  const pathname = usePathname();
  const { data: session } = useSession();

  // Don't show on auth pages
  if (['/login', '/register', '/reset-password'].includes(pathname)) return null;

  return (
    <nav className="md:hidden fixed bottom-0 left-0 right-0 z-50 glass border-t border-white/10"
         style={{ paddingBottom: 'env(safe-area-inset-bottom, 0px)' }}>
      <div className="flex items-center justify-around h-16">
        {navItems.map((item) => {
          const active = pathname === item.href;
          const href = item.href === '/dashboard' && !session ? '/login' : item.href;
          return (
            <Link
              key={item.href}
              href={href}
              className="flex flex-col items-center gap-0.5 py-1 px-3"
            >
              <NavIcon icon={item.icon} active={active} />
              <span className={`text-[10px] ${active ? 'text-gold' : 'text-white/40'}`}>
                {item.label}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
